# Python program to print Even and odd Numbers in a List
'''
list1 = [10, 21, 4, 45, 66, 93, 11,2]
#lambda expression 
# filter--The filter function returns a sequence of those elements of iterable object for which function returns true value.
# labmda-- no need to use def to create a function and Can accept any number of arguments but returns only one value in the form of expression.
# Syntax:		lambda arguments : expression
even_nos = list(filter(lambda x: (x % 2 == 0), list1))
odd_nos = list(filter(lambda x: (x % 2 != 0), list1))

print("Odd numbers in the list: ", odd_nos)
print("Even numbers in the list: ", even_nos)
'''






















































